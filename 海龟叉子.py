import turtle
t=turtle.Pen()
kate=turtle.Pen()
marry=turtle.Pen()
piter=turtle.Pen()
def turtle_pen():
        t.reset()
        kate.reset()
        marry.reset()
        piter.reset()
        t.forward(110)
        t.left(90)
        t.forward(35)
        t.right(90)
        t.forward(35)
        kate.forward(100)
        kate.left(90)
        kate.forward(60)
        kate.right(90)
        kate.forward(75)
        marry.forward(110)
        marry.right(90)
        marry.forward(35)
        marry.left(90)
        marry.forward(35)
        piter.forward(100)
        piter.right(90)
        piter.forward(60)
        piter.left(90)
        piter.forward(75)
turtle_pen()
input('按任意键退出')
